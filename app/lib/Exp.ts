function getRandomItems(items: any[], count: number) {
  // Clone the items array to avoid mutating the original array
  let tempItems = [...items];

  // The result array to store the random items
  let result: any[] = [];

  // Ensure the count is not greater than the array length
  count = Math.min(count, items.length);

  for (let i = 0; i < count; i++) {
    // Generate a random index based on the length of tempItems
    let randomIndex = Math.floor(Math.random() * tempItems.length);

    // Push the item at the random index to the result array
    result.push({item: tempItems[randomIndex], index: randomIndex + 1});

    // Remove the item from the tempItems array to avoid duplicates
    tempItems.splice(randomIndex, 1);
  }

  return result;
}

export function truncateText(text: string, maxLength: number = 44): string {
  if (text.length > maxLength) {
    return text.substring(0, maxLength) + "..";
  } else {
    return text;
  }
}
export function getYoutubeId(url: string): string {
  if (!url) return "null";
  if (!url.includes("https://")) return "null";
  const parts = url.split("/");
  return parts.pop() || "";
}
